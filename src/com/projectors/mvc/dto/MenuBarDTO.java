package com.projectors.mvc.dto;

public class MenuBarDTO
{
	// 주요 변수 선언
	private String pinNo;
	private int count;
	// getter, setter 생성
	public String getPinNo()
	{
		return pinNo;
	}
	public void setPinNo(String pinNo)
	{
		this.pinNo = pinNo;
	}
	public int getCount()
	{
		return count;
	}
	public void setCount(int count)
	{
		this.count = count;
	}
}
